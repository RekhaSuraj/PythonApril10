str1 = "Mangoes are Sweet"

# upper() - Converts all the letters in the string to upper case(capitals)
u = str1.upper()
print(u)
# MANGOES ARE SWEET

# lower - Converts all the letters in the string to lower case(small letter)
l = str1.lower()
print(l)
# mangoes are sweet

# casefold() - Return a version of the string suitable for caseless comparisons.
# Converts to lowercase(), but casefold is more aggressive
print(str1.casefold())
# mangoes are sweet

