s1 = "hello world how are you"
# title() - Return a version of the string where each word is titlecased.
# More specifically, words start with uppercased characters and all remaining cased characters have lower case

title_s = s1.title()
# Hello World How Are You

# istitle() - Returns True if string is in title case, else Returns False
print(s1.istitle())
# False

print(title_s.istitle())
# True

# isupper() - Returns True if string is in upper case, else returns False
print(s1.isupper())

s2 = "WELCOMe"
print(s2.isupper())
# False

# islower() - Returns True is string is in lower case, else returns False
print(s1.islower())
# True

