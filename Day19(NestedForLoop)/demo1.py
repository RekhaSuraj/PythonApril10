# nested for loop
# A for loop inside a for loop is called a nested for loop. The inner loop runs completely for each iteration
# of the outer loop.
#
# for i in range(outer_loop_range):
# #     for j in range(inner_loop_range):
        # Code block to execute


for i in range(3):
    for j in range(3):
        print(i,j)

