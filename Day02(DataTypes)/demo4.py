# conversions from one datatype to another

# Implicit type casting or conversion
# What is typecasting - conversion from one type to another
# When we add an int and float numbers, interpreter converts the answer into float so that we dont lose data
# In the below example, 20 and 10.5 addition gives 30.5 in float.
a = 20
b = 10.5

s = a+b
print(s)
# 30.5


