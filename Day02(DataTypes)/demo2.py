# complex number - complex numbers are used for performing complex mathematical operations
# It contains a real part and an imaginary part
# Ex: 3+4j

# In the above example, 3 is real part
# 4 is the imaginary part

v = 3+4j
print(v)
# (3+4j)
print(type(v))
# <class 'complex'>

print(v.real)
# 3.0

c = v.imag
print(c)
# 4.0


