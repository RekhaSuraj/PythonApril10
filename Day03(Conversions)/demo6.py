# Floor Division (//) - It divides the numbers and gives us the quotient removing/ignoring the decimal part
# rounds the quotient to the nearest number (approximate value) 
# ignores the decimal points

a = 90
b = 9
f = a//b
print(f)
# 10

x = 9
y = 2
f1 = x//y
print(f1)
# 4

