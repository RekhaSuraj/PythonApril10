# Conversions - Convert from decimal to binary
# Binary numbers are 0 and 1, it will have a prefix of 0b. 
# This prefix indicates that it is a binary number
# Syntax : bin(number)


a = 17
# 10001
print(bin(a))
# 0b10001

b = 34
print(bin(b))
# 0b100010

print(bin(29)) # hardcoding the number
# 0b11101

# Conversion from binary to decimal, just give the binary value with prefix 0b
print(0b11101)
# 29

print(0b100010)
# 34