# conversion from decimal to Octal 
# It is prefixed with 0o
# It contains numbers from 0 to 7
# Syntax: oct(decimalnumber)


v = 50
print(oct(v))
# 0o62


x = 99
print(oct(x))
# 0o143

# Conversion from octal to decimal

l = 0o143
print(l)
# 99

# print(0o99)
# SyntaxError: invalid digit '9' in octal literal

print(0o62)
