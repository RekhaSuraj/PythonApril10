# Lesser than or Equal to (<=) - REturns True if a is lesser than or Equal to b, else returns False

a = 50
b = 90
print(a<=b)
# True

print(40<=40)
# True

print(70<=30)
# False

# Not Equal to (!=) - Returns True if x is not equal to y, else returns False
x = 20
y = 20
print(x!=y)
# False

m = 40
n = 30
print(m!=n)
# True


