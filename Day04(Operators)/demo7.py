# Comparison operators - Returns boolean value
# == (Equal to)
# > (Greater than)
# < (Lesser than)
# >= (Greater than or Equal to)
# <= (Lesser than or Equal to)
# != (Not Equal to)

# Equal to - Compares and returns True if both values are same,else returns False
a = 10
b = 10
print(a==b)
# True

print(5==6) # Hard coding
# False

# Greater than - Returns True if x is greater than y, else returns False
x = 30
y = 20
print(x>y)
# True

# Lesser than - Returns True if a is lesser than b, else returns False
a = 10
b = 20

print(a<b)
# True

print(30<10)
# False

print(-20<10)
# True

