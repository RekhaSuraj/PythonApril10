# Floor division and assign (//=)

f = 25
f //= 2

# is same as f = f//2, 25//2

print('FLoor division and assign:',f)
# FLoor division and assign: 12

# Modulus and assign (%=)

m = 50
m %= 3

# is same as m = m%3, m = 50%3
print('Modulus and assign',m)
# Modulus and assign 2


# Exponentiation and assign(**=)
e = 4
e **= 3

# is same as e = e**3, e = 4**3
print('Exponentiation and assign:',e)
# Exponentiation and assign: 64