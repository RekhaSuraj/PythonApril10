# Types of creating a variable
# Assign single value to a single variable
# a = 10
# print(a)

# output
# 10

# single value assigned to multiple variables
x=y=z=l=30
# print('x',x)
# print('y',y)
# print('z',z)
# print('l',l)

# output
# x 30
# y 30
# z 30
# l 30

# multiple values assigned to multiple variables in a single line separated by semicolon
x=10; y=20; z=30; k=40
# print('x:',x)
# print('y:',y)
# print('z:',z)
# print('k:',k)

# output
# x: 10
# y: 20
# z: 30
# k: 40


# multiple values assigned to multiple variables in a single line without using semicolon
l,m,n = 50,60,70
print('l:',l)
print('m:',m)
print('n:',n)

# output
l: 50
m: 60
n: 70
