# Strings - Strings are a sequence of characters(letters, alphabets) 
# enclosed within single quote or double quotes
# Ex : 'Welcome to Python learning'

s = 'Welcome to Python'
print(type(s))
# <class 'str'>

s1 = "Hello World"
print(type(s1))
# <class 'str'>

# Empty string
s2 = " "
print(type(s2))
# <class 'str'>

