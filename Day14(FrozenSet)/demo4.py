d1 = {1:'Mango',2:'Orange',3:'Banana',4:'Apple'}
# To fetch key, values and items
# d1.keys() - a set-like object providing a view on D's keys
print(d1.keys())
# dict_keys([1, 2, 3, 4])

# d1.values() - an object providing a view on D's values
print(d1.values())
# dict_values(['Mango', 'Orange', 'Banana', 'Apple'])

print(d1.items())
# dict_items([(1, 'Mango'), (2, 'Orange'), (3, 'Banana'), (4, 'Apple')])