# Frozenset -
# A frozenset in Python is an immutable version of a set. Once created,
# its elements cannot be changed, added, or removed.
# Does not retain insertion order, index not supported

# How to create a frozenset
fz = frozenset([10,20,30,'hi','hello'])
print(type(fz))
# <class 'frozenset'>

# How to create an empty frozenset
fz1 = frozenset()
print(type(fz1))
# <class 'frozenset'>

